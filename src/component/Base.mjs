    afterSetTooltips(value, oldValue) {
        if (value) {
            let me = this;

            if (Neo.ns('Neo.tooltip.Base')) {
                me.createTooltips(value);
            } else {
                import(
                    '../tooltip/Base.mjs').then((module) => {
                        me.createTooltips(value);
                    }
                );
            }
        }
    }

